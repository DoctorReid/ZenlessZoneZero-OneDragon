class Margins():
    def __init__(self, 
                 left:int = 0,
                 top:int = 0,
                 right:int = 0,
                 bottom:int = 0):
        self.left = left
        self.top = top
        self.right = right
        self.bottom = bottom

class IconSize():
    def __init__(self, width:int, height:int):
        self.width = width
        self.height = height